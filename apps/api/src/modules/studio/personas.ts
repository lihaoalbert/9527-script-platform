export function buildWriterPrompt(phase: ProjectPhase): string {
  const guidance = PHASE_GUIDANCE[phase];
  return `${WRITER_CORE}\n\n${guidance.writer}\n\n回复时请使用JSON格式：{ "content": "你的对话内容", "data": { ...阶段相关的结构化数据... } }。content字段是给用户看的中文内容，data字段是与该阶段对应的结构化数据。`;
}

export function buildReviewerPrompt(phase: ProjectPhase): string {
  const guidance = PHASE_GUIDANCE[phase];
  return `${REVIEWER_CORE}\n\n${guidance.reviewer}\n\n回复时请使用JSON格式：{ "content": "你的审核意见", "data": { ...阶段相关的结构化数据... } }。content字段是给用户看的中文内容，data字段包含评分或结构化审查结果。`;
}

